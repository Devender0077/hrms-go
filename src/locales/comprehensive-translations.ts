// Merge function to combine comprehensive translations with existing ones
export function mergeTranslations(existing: any, comprehensive: any) {
  const merged: any = {};
  
  for (const lang in existing) {
    merged[lang] = {
      ...existing[lang],
      ...(comprehensive[lang] || {})
    };
  }
  
  return merged;
}

